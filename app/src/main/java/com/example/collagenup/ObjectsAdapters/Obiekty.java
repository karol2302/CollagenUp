package com.example.collagenup.ObjectsAdapters;

public class Obiekty {
    public String czas = "";
    public String zabieg = "";
    public String trwanie = "";
    public String nazwisko = "";

    public Obiekty(String n, String c, String z, String t) {
        this.nazwisko = n;
        this.czas = c;
        this.trwanie = t;
        this.zabieg = z;
    }

}
